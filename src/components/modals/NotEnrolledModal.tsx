"use client";

import { useRouter } from "next/navigation";

interface NotEnrolledModalProps {
  onClose: () => void;
  redirectTo?: string | null;
}

export default function NotEnrolledModal({ onClose, redirectTo }: NotEnrolledModalProps) {
  const router = useRouter();

  const handleOk = () => {
    onClose();
    if (redirectTo) {
      router.push(redirectTo);
    }
  };

  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/50">
      <div className="mx-4 w-full max-w-md rounded-xl bg-white p-6 shadow-xl">
        <div className="mb-4 flex items-center gap-3">
          <div className="flex h-12 w-12 items-center justify-center rounded-full bg-amber-100">
            <svg
              className="h-6 w-6 text-amber-600"
              fill="none"
              viewBox="0 0 24 24"
              stroke="currentColor"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={2}
                d="M12 9v2m0 4h.01m-6.938 4h13.856c1.54 0 2.502-1.667 1.732-3L13.732 4c-.77-1.333-2.694-1.333-3.464 0L3.34 16c-.77 1.333.192 3 1.732 3z"
              />
            </svg>
          </div>
          <h3 className="text-xl font-semibold text-gray-900">Access restricted</h3>
        </div>
        <p className="mb-6 text-gray-600">
          You must enroll in this course before you can access the test. Please enroll first to
          continue.
        </p>
        <button
          onClick={handleOk}
          className="w-full rounded-lg bg-blue-600 px-4 py-2.5 font-semibold text-white transition hover:bg-blue-700 focus:outline-none focus:ring-2 focus:ring-blue-500 focus:ring-offset-2"
        >
          Ok, I understand!
        </button>
      </div>
    </div>
  );
}
